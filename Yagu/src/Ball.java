public class Ball {
   private String question;
   private int strike;
   private int ball;

   public String getQuestion() {
      return question;
   }
   public void setQuestion(String question) {
      this.question = question;
   }
   public int getStrike() {
      return strike;
   }
   public void setStrike(int strike) {
      this.strike = strike;
   }
   public int getBall() {
      return ball;
   }
   public void setBall(int ball) {
      this.ball = ball;
   }
}